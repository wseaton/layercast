def encode(msg: IpcRequest) -> bytes:
    """Serialize an IpcRequest to length-prefixed protobuf bytes."""
    payload = bytes(msg)
    return struct.pack(">I", len(payload)) + payload


def decode_response(data: bytes) -> IpcResponse:
    """Deserialize a daemon response from protobuf bytes."""
    return IpcResponse().parse(data)
